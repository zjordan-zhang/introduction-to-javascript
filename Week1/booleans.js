console.log("1 < 2 is", 1 < 2);

console.log("1 > 2 is", 1 > 2);

// Booleans only have two outcomes - true or false

console.log("1 == 2 is", 1 == 2);

console.log("100 == '100' is", 100 == '100');

// Strict equality operator - checks both the value and the type
console.log("100 === '100' is", 100 === '100');

console.log("1 != '1' is", 1 != '1');

console.log("1 !== '1' is", 1 !== '1');
